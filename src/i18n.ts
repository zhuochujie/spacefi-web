export function getLanguage() {
    return i18next.language as AppLanguage
}

export function setLanguage(language: AppLanguage) {
    localStorage.setItem(LANGUAGE_KEY, language)
    return i18next.changeLanguage(language)
}

export function getApiErrorKey(message: string) {
    return apiErrorKeyMap[message] || message
}

const walletErrorPatterns = [
    'user rejected',
    'user denied',
    'rejected',
    'denied',
    'insufficient funds',
    'execution reverted',
    'contractfunctionexecutionerror',
    'transactionexecutionerror',
    'estimate gas',
    'shortmessage',
    'details:',
    'reason:',
    'connector',
    'wallet',
    'chain',
]

export function getFriendlyErrorKey(message: string, fallbackKey: string) {
    const normalizedMessage = message.toLowerCase()

    if (walletErrorPatterns.some((pattern) => normalizedMessage.includes(pattern))) {
        return fallbackKey
    }

    const apiErrorKey = getApiErrorKey(message)

    if (fallbackKey && apiErrorKey === message) {
        return fallbackKey
    }

    return apiErrorKey
}

export function translate(key: string, options?: Record<string, unknown>) {
    return i18next.t(key, options)
}

export function useI18n() {
    const [language, setCurrentLanguage] = useState(getLanguage())

    useEffect(() => {
        const handleLanguageChanged = (nextLanguage: string) => {
            setCurrentLanguage(nextLanguage as AppLanguage)
        }

        i18next.on('languageChanged', handleLanguageChanged)

        return () => {
            i18next.off('languageChanged', handleLanguageChanged)
        }
    }, [])

    return {
        language,
        setLanguage,
        t: translate,
    }
}

export default i18next
